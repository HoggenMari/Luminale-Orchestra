	public LinkedList<Nozzle> breadthFirstSearch(Nozzle start, Nozzle target) {
		String weg = "";
		LinkedList<Nozzle> path = new LinkedList<Nozzle>();
		resetMarked();
		int[] predecessor = new int[nozzleList.size()];
		for(int i=0; i<nozzleList.size(); i++){
			predecessor[i] = -1;
		}
		predecessor[start.id] = start.id;
		LinkedList<Nozzle> queue = new LinkedList<Nozzle>();
		start.marked = true;
		queue.add(start);
		boolean found = false;
		while(!queue.isEmpty() && !found) {
			Nozzle kq = queue.removeFirst();
			//System.out.println(kq.id);
			for(Nozzle n : kq.neighbour) {
				if(!n.marked) {
					predecessor[n.id] = kq.id;
					n.marked = true;
					if(n.id==target.id) {
						found = true;
					}
					queue.add(n);
				}
			}
		}
		if (queue.isEmpty()) {
			weg = "kein Weg";
			return path;
		}else{
			int j = target.id;
			do{
				path.add(nozzleList.get(j));
				weg = "->"+j+weg;
				j = predecessor[j];
			} while (j != start.id);
		weg = "Weg gefunden: "+start.id+weg;
		path.add(nozzleList.get(start.id));
		}
		//System.out.println(weg);
		return path;
	}
	
